import random

class LootList:
    def __init__(self, min_gold, max_gold, min_exp, max_exp, chance, items):
        self.gold = [min_gold, max_gold]
        self.exp = [min_exp, max_exp]
        self.chance = chance
        self.items = items
    
    def getGold(self):
        if self.gold[0] + self.gold[1] > 0:
            return random.randint(self.gold[0], self.gold[1])
        else:
            return 0
    
    def getItems(self):
        loot = []
        
        for item in self.items:
            roll = random.randint(0,100)
            if roll <= self.chance:
                loot.append(item)
        
        return loot

    def getRandomItem(self):
        return random.choice(self.items)
    
    def getExp(self):
        return random.randint(self.exp[0], self.exp[1])


